# MAIOR QUE / MAIOR OU IGUAL
# O retorno é sempre um valor booleano(verdadeiro ou falso)


# MAIOR QUE

saldo = 450
saque = 200

print(f"saldo > saque: {saldo > saque}")  # verdadeiro

a = 400
b = 800

print(f"a > b: {a > b}")  # falso

# MAIOR OU IGUAL

saldo = 450
saque = 200

print(f"saldo >= saque: {saldo >= saque}")  # verdadeiro

x = 100
y = 900

print(f"x >= y: {x >= y}")  # falso

a = 400
b = 400

print(f"a >= b: {a >= b}")  # verdadeiro
