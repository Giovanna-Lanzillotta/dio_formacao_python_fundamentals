# MENOR QUE / MENOR OU IGUAL
# O retorno é sempre um valor booleano(verdadeiro ou falso)


# MENOR QUE

saldo = 450
saque = 200

print(f"saldo < saque: {saldo < saque}")  # falso

a = 400
b = 800

print(f"a < b: {a < b}")  # verdadeiro

# MENOR OU IGUAL

saldo = 450
saque = 200

print(f"saldo <= saque: {saldo <= saque}")  # falso

x = 100
y = 900

print(f"x <= y: {x <= y}")  # verdadeiro

a = 400
b = 400

print(f"a <= b: {a <= b}")  # verdadeiro
