# ATRIBUIÇÃO SIMPLES

saldo = 500
print(f'atribuição simples: {saldo}')


# ATRIBUIÇÃO COM ADIÇÃO

saldoAdicao = 500
saldoAdicao += 200
print(f'atribuição com adição: {saldoAdicao}')


# ATRIBUIÇÃO COM SUBTRAÇÃO

saldoSubtracao = 500
saldoSubtracao -= 200
print(f'atribuição com subtração: {saldoSubtracao}')


# ATRIBUIÇÃO COM MULTIPLICAÇÃO

saldoMultiplicacao = 500
saldoMultiplicacao *= 2
print(f'atribuição com multiplicacao: {saldoMultiplicacao}')


# ATRIBUIÇÃO COM DIVISÃO

saldoDivisao = 500
saldoDivisao /= 5
print(f'atribuição com divisão: {saldoDivisao}')

saldoDivisaoInteira = 500
saldoDivisaoInteira //= 5
print(f'atribuição com divisão inteira: {saldoDivisaoInteira}')


# ATRIBUIÇÃO COM MÓDULO

saldoModulo = 500
saldoModulo %= 480
print(f'atribuição com módulo: {saldoModulo}')


# ATRIBUIÇÃO COM EXPONENCIAÇÃO

saldoModulo = 80
saldoModulo **= 2
print(f'atribuição com exponenciação: {saldoModulo}')