# OPERADORES DE ASSOCIAÇÃO

curso = "Curso de Python"
frutas = ["laranja","uva","limão"]
saques = [1500, 100]

print(f"Python in curso ?? {'Python' in curso}") #true
print(f"Java in curso ?? {'Java' in curso}") #false

print("\n")

print(f"maçã in frutas ??? {'maçã' in frutas}") #false
print(f"uva in frutas ??? {'uva' in frutas}") #true

print("\n")

print(f"200 in saques ??? {200 in saques}") #false
print(f"100 in saques ??? {100 in saques}") #true
