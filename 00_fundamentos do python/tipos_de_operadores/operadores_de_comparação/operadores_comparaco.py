saque = 200

print(f"saldo é igual de saque?? : {saldo == saque}") # True
print(f"saldo é diferente de saque?? : {saldo != saque}") # False
print(f"saldo é maior do que de saque?? : {saldo < saque}") # False
print(f"saldo é maior ou igual a saque?? : {saldo >= saque}") # True
print(f"saldo é menor de que saque?? : {saldo < saque}") # False
print(f"saldo é menor ou igual a saque?? : {saldo <= saque}") # True



